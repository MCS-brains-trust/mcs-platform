@login_required
@require_POST
def legal_template_upload(request):
    """Upload a new legal document template."""
    name = request.POST.get("name", "")
    doc_type = request.POST.get("document_type", "")
    entity_types = request.POST.getlist("entity_types")
    file = request.FILES.get("template_file")

    if not file or not doc_type:
        return JsonResponse({"error": "File and document type are required."}, status=400)

    # Check if template already exists for this type
    existing = LegalDocumentTemplate.objects.filter(
        document_type=doc_type, is_active=True
    ).first()

    if existing:
        # Version up
        existing.is_active = False
        existing.save(update_fields=["is_active"])
        version = existing.version + 1
    else:
        version = 1

    template = LegalDocumentTemplate.objects.create(
        name=name or dict(LegalDocumentTemplate.DocumentType.choices).get(doc_type, doc_type),
        document_type=doc_type,
        entity_types=entity_types,
        template_file=file,
        version=version,
        created_by=request.user,
    )

    # Extract variable schema from template
    try:
        schema = _extract_template_variables(template)
        template.variable_schema = schema
        template.save(update_fields=["variable_schema"])
    except Exception as e:
        logger.warning("Failed to extract template variables: %s", e)

    return JsonResponse({"status": "ok", "template_id": str(template.pk)})


