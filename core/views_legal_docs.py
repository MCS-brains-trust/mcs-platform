def _extract_template_variables(template):
    """Extract {{variable}} placeholders from a docxtpl template."""
    import re
    try:
        from docxtpl import DocxTemplate
        tpl = DocxTemplate(template.template_file.path)
        # Get the XML content
        xml_content = tpl.get_xml()
        # Find all {{variable}} patterns
        variables = set(re.findall(r'\{\{([\w.]+)\}\}', xml_content))
        # Also find {% for %} loop variables
        loops = re.findall(r'\{%\s*for\s+(\w+)\s+in\s+(\w+)\s*%\}', xml_content)

        schema = {
            "variables": sorted(variables),
            "loops": [{"item": l[0], "collection": l[1]} for l in loops],
        }
        return schema
    except Exception as e:
        logger.warning("Failed to extract template variables: %s", e)
        return {"variables": [], "loops": []}


