@login_required
@require_POST
def engagement_letter_generate(request, pk):
    """Generate engagement letter via DocumentContextBuilder for correct selected_services."""
    entity = get_entity_for_user(request, pk)

    params = json.loads(request.body) if request.content_type == "application/json" else request.POST.dict()

    financial_year_id = params.get("financial_year_id")
    fy = get_object_or_404(FinancialYear, pk=financial_year_id, entity=entity) if financial_year_id else None

    template = LegalDocumentTemplate.objects.filter(
        document_type="engagement_letter", is_active=True
    ).first()
    if not template:
        return JsonResponse({"error": "No active engagement letter template."}, status=404)

    _logger = logging.getLogger(__name__)
    _logger.info("EL wizard_data: %s", params)

    from core.document_context_builder import DocumentContextBuilder
    dcb = DocumentContextBuilder(entity, financial_year=fy, wizard_data=params)
    context = dcb.build("engagement_letter")
    _logger.info("EL context selected_services: %s", context.get('selected_services'))

    result = render_and_create_document(
        entity=entity,
        financial_year=fy,
        template=template,
        doc_type="engagement_letter",
        context=context,
        params=params,
        user=request.user,
        disclaimer_acknowledged=params.get("disclaimer_acknowledged", False),
    )

    if result.get("status") == "ok":
        # Save sanitised context_data for re-rendering
        from core.views_partnership_docs import _sanitise_context_for_storage
        doc_id = result.get("document_id")
        if doc_id:
            LegalDocument.objects.filter(pk=doc_id).update(
                context_data=_sanitise_context_for_storage(context)
            )
        return JsonResponse({
            "status": "ok",
            "document_id": result.get("document_id"),
            "docx_url": result.get("docx_url"),
            "pdf_url": result.get("pdf_url"),
            "message": "Engagement letter saved successfully.",
            "redirect_url": None,
        })
    return JsonResponse({"error": result.get("error", "Generation failed.")}, status=500)


@login_required
@require_POST
def legal_doc_generate(request, pk, doc_type):
    """
    Generate a legal document from template + wizard inputs.
    Routes to document-type-specific generators when available.
    """
    fy = get_financial_year_for_user(request, pk)
    entity = fy.entity

    # Route to specific generators
    SPECIFIC_GENERATORS = {
        "engagement_letter": engagement_letter_generate,
        "div7a_loan_agreement": div7a_generate,
        "trust_deed_change_trustee": change_of_trustee_generate,
        "unit_trust_deed": fixed_unit_trust_generate,
        "unit_transfer": unit_transfer_generate,
    }

    specific_view = SPECIFIC_GENERATORS.get(doc_type)
    if specific_view:
        return specific_view(request, pk)

    # Fall back to generic generation
    template = LegalDocumentTemplate.objects.filter(
        document_type=doc_type, is_active=True
    ).first()

    if not template:
        return JsonResponse({"error": "No active template found."}, status=404)

    # Merge entity context with user-provided parameters
    parameters = json.loads(request.body) if request.content_type == "application/json" else request.POST.dict()
    entity_context = _build_entity_context(entity, fy)
    entity_context.update(parameters)

    # Add standard fields
    entity_context["disclaimer"] = STANDARD_DISCLAIMER
    entity_context["generation_date"] = timezone.now().strftime("%d %B %Y")
    # Firm branding from FirmSettings (white-label support)
    try:
        from core.models import FirmSettings
        _fs = FirmSettings.get()
        entity_context["firm_name"] = _fs.firm_name or "MC & S Chartered Accountants"
        entity_context["firm_logo_url"] = _fs.logo_url or ""
        entity_context.setdefault("firm_address_1", _fs.firm_address_1 or "")
        entity_context.setdefault("firm_address_2", _fs.firm_address_2 or "")
        entity_context.setdefault("firm_phone", _fs.firm_phone or "")
        entity_context.setdefault("firm_email", _fs.firm_email or "")
    except Exception:
        entity_context["firm_name"] = "MC & S Chartered Accountants"
        entity_context["firm_logo_url"] = ""

    result = render_and_create_document(
        entity=entity,
        financial_year=fy,
        template=template,
        doc_type=doc_type,
        context=entity_context,
        params=parameters,
        user=request.user,
        disclaimer_acknowledged=parameters.get("disclaimer_acknowledged", False),
    )

    if result["status"] == "ok":
        return JsonResponse(result)
    else:
        return JsonResponse({"error": result.get("error", "Generation failed.")}, status=500)


